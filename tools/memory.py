def format_profile_for_context() -> str:
    data = load_profile()
    facts = data.get("facts", {})
    profile = data.get("profile", "")
    updated = data.get("last_updated", "never")

    lines: list[str] = []

    # --- Structured facts (always shown when present) ---
    personal = facts.get("personal", {})
    personal_parts = []
    if personal.get("name"):
        personal_parts.append(f"Name: {personal['name']}")
    if personal.get("age"):
        personal_parts.append(f"Age: {personal['age']}")
    if personal.get("weight_lbs"):
        personal_parts.append(f"Weight: {personal['weight_lbs']}lbs")
    if personal_parts:
        lines.append("  " + " | ".join(personal_parts))
    if personal.get("family_context"):
        lines.append(f"  Family: {personal['family_context']}")

    race = facts.get("race", {})
    if race.get("name") or race.get("date"):
        race_str = f"  Race: {race.get('name', 'TBD')}"
        if race.get("date"):
            race_str += f" on {race['date']}"
        details = []
        if race.get("distance_miles"):
            details.append(f"{race['distance_miles']}mi")
        if race.get("elevation_gain_ft"):
            details.append(f"~{race['elevation_gain_ft']:,}ft gain")
        if race.get("course_type"):
            details.append(race["course_type"])
        if details:
            race_str += f" ({', '.join(details)})"
        lines.append(race_str)

    training = facts.get("training", {})
    if training.get("weekly_mileage_target"):
        lines.append(f"  Weekly mileage target: {training['weekly_mileage_target']}mi/week")

    active_injuries = [inj for inj in facts.get("injuries", []) if not inj.get("resolved")]
    if active_injuries:
        lines.append("  Active injuries:")
        for inj in active_injuries:
            lines.append(f"    - {inj['description']} (first noted {inj['first_mentioned']})")

    prefs = training.get("preferences", [])
    if prefs:
        lines.append("  Coaching preferences:")
        for pref in prefs:
            lines.append(f"    - {pref}")

    last_week = facts.get("last_week", {})
    if last_week.get("planned_miles") is not None or last_week.get("actual_miles") is not None:
        lines.append(
            f"  Last week: planned {last_week.get('planned_miles', '?')}mi, "
            f"actual {last_week.get('actual_miles', '?')}mi"
        )

    if lines:
        lines.insert(0, "  [Confirmed facts]")
        lines.append("")

    # --- Gaps (shown so Claude knows what to ask about) ---
    gaps = []
    if not race.get("name"):
        gaps.append("target race name")
    if not race.get("date"):
        gaps.append("race date")
    if not race.get("distance_miles"):
        gaps.append("race distance")
    if not race.get("elevation_gain_ft"):
        gaps.append("race elevation gain")
    if not training.get("weekly_mileage_target"):
        gaps.append("weekly mileage target")
    if not personal.get("age"):
        gaps.append("athlete age")
    if not personal.get("weight_lbs"):
        gaps.append("athlete weight")
    if gaps:
        lines.append(f"  [Profile gaps — ask about one per conversation: {', '.join(gaps)}]")
        lines.append("")

    # --- Narrative profile (monthly synthesis) ---
    if profile:
        lines.append(f"  [Coaching profile — last updated {updated}]")
        lines.append(f"  {profile}")
    elif not lines:
        return "  No athlete profile built yet."

    return "\n".join(lines)


def format_recent_memos_for_context() -> str:
    memos = load_memos()
    if not memos:
        return "  No weekly memos yet — builds after first full week."
    recent = memos[-MEMOS_IN_CONTEXT:]
    lines = []
    for memo in recent:
        lines.append(f"  Week of {memo['week_of']}: {memo['memo']}")
    return "\n\n".join(lines)


