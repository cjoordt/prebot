"""
tools/memory.py — Persistent coaching memory

Two layers:
  1. Weekly memos — narrative arc of what happened each week
  2. Athlete profile — evolving model of the athlete: patterns, tendencies, history

Both are generated by Claude and included in every agent context call.
"""

from __future__ import annotations

import copy
import json
import logging
import os
from datetime import timedelta
from pathlib import Path

from utils import local_now

import anthropic
from dotenv import load_dotenv

load_dotenv()

logger = logging.getLogger(__name__)

PROFILE_FILE = Path(__file__).parent.parent / "data" / "athlete_profile.json"
NOTES_FILE = Path(__file__).parent.parent / "data" / "coaching_notes.json"
MODEL = "claude-opus-4-5"
EXTRACT_MODEL = "claude-haiku-4-5-20251001"
MEMOS_IN_CONTEXT = 6  # how many recent weekly memos to include in every message

client = anthropic.Anthropic(api_key=os.getenv("ANTHROPIC_API_KEY"))

_EMPTY_FACTS: dict = {
    "personal": {
        "name": None,
        "age": None,
        "weight_lbs": None,
        "family_context": None,
    },
    "race": {
        "name": None,
        "date": None,
        "distance_miles": None,
        "elevation_gain_ft": None,
        "course_type": None,
    },
    "training": {
        "weekly_mileage_target": None,
        "preferences": [],
    },
    "injuries": [],   # [{description, first_mentioned, resolved}]
    "last_week": {
        "planned_miles": None,
        "actual_miles": None,
    },
}


# ---------------------------------------------------------------------------
# Persistence
# ---------------------------------------------------------------------------

def load_profile() -> dict:
    if not PROFILE_FILE.exists():
        return {"last_updated": None, "profile": "", "facts": copy.deepcopy(_EMPTY_FACTS)}
    with open(PROFILE_FILE, "r") as f:
        data = json.load(f)
    if "facts" not in data:
        data["facts"] = copy.deepcopy(_EMPTY_FACTS)
    return data


def save_profile(data: dict) -> None:
    with open(PROFILE_FILE, "w") as f:
        json.dump(data, f, indent=2)


def load_facts() -> dict:
    return load_profile().get("facts", copy.deepcopy(_EMPTY_FACTS))


def save_facts(facts: dict) -> None:
    data = load_profile()
    data["facts"] = facts
    save_profile(data)


def _merge_facts(existing: dict, new_facts: dict) -> dict:
    """Merge extracted facts into existing. Scalars overwrite, lists append (deduped)."""
    result = copy.deepcopy(existing)

    # personal — overwrite any non-None value
    for key in ("name", "age", "weight_lbs", "family_context"):
        val = new_facts.get("personal", {}).get(key)
        if val is not None:
            result["personal"][key] = val

    # race — overwrite any non-None value
    for key in ("name", "date", "distance_miles", "elevation_gain_ft", "course_type"):
        val = new_facts.get("race", {}).get(key)
        if val is not None:
            result["race"][key] = val

    # training — mileage target overwrites; preferences append (deduped)
    target = new_facts.get("training", {}).get("weekly_mileage_target")
    if target is not None:
        result["training"]["weekly_mileage_target"] = target
    for pref in new_facts.get("training", {}).get("new_preferences", []):
        if pref and pref not in result["training"]["preferences"]:
            result["training"]["preferences"].append(pref)

    # injuries — append new (deduped by description), mark resolved
    existing_descs = {inj["description"].lower() for inj in result.get("injuries", [])}
    for inj in new_facts.get("new_injuries", []):
        desc = inj.get("description", "").strip()
        if desc and desc.lower() not in existing_descs:
            result["injuries"].append({
                "description": desc,
                "first_mentioned": inj.get("first_mentioned", local_now().strftime("%Y-%m-%d")),
                "resolved": False,
            })
            existing_descs.add(desc.lower())
    for resolved_desc in new_facts.get("resolved_injuries", []):
        for inj in result["injuries"]:
            if inj["description"].lower() == resolved_desc.lower():
                inj["resolved"] = True

    # last_week
    for key in ("planned_miles", "actual_miles"):
        val = new_facts.get("last_week", {}).get(key)
        if val is not None:
            result["last_week"][key] = val

    return result


def extract_and_update_facts(user_text: str, assistant_text: str) -> None:
    """
    Extract memorable facts from a conversation exchange and merge into the profile.
    Designed to fail silently — never blocks the main conversation flow.
    """
    try:
        facts = load_facts()
        today = local_now().strftime("%Y-%m-%d")

        prompt = (
            "Extract key facts from this coaching conversation. "
            "Return ONLY a JSON object with the fields you found — omit anything not explicitly mentioned.\n\n"
            "Schema (all fields optional):\n"
            "{\n"
            '  "personal": {"name": str, "age": int, "weight_lbs": float, "family_context": str},\n'
            '  "race": {"name": str, "date": "YYYY-MM-DD", "distance_miles": float, '
            '"elevation_gain_ft": int, "course_type": str},\n'
            '  "training": {"weekly_mileage_target": float, "new_preferences": [str]},\n'
            '  "new_injuries": [{"description": str, "first_mentioned": "YYYY-MM-DD"}],\n'
            '  "resolved_injuries": [str],\n'
            '  "last_week": {"planned_miles": float, "actual_miles": float}\n'
            "}\n\n"
            f"Today: {today}\n\n"
            f"User: {user_text}\n\n"
            f"Coach: {assistant_text}\n\n"
            "Return {} if nothing worth remembering was mentioned. Return only valid JSON, no prose."
        )

        response = client.messages.create(
            model=EXTRACT_MODEL,
            max_tokens=300,
            messages=[{"role": "user", "content": prompt}],
        )

        raw = response.content[0].text.strip()
        if not raw or raw == "{}":
            return

        new_facts = json.loads(raw)
        if new_facts:
            merged = _merge_facts(facts, new_facts)
            save_facts(merged)
            logger.info("Athlete facts updated from conversation.")

    except Exception as e:
        logger.warning(f"Fact extraction failed (non-critical): {e}")


def load_memos() -> list[dict]:
    if not NOTES_FILE.exists():
        return []
    with open(NOTES_FILE, "r") as f:
        return json.load(f).get("memos", [])


def save_memos(memos: list[dict]) -> None:
    with open(NOTES_FILE, "w") as f:
        json.dump({"memos": memos}, f, indent=2)


def append_memo(memo: dict) -> None:
    memos = load_memos()
    memos.append(memo)
    save_memos(memos)
    logger.info(f"Coaching memo saved for week of {memo.get('week_of')}.")


